#!/usr/bin/env python3
"""Generate HexEngine's dependency SBOM + third-party license notices.

Reads the hand-curated inventory (tools/deps/dependencies.json), cross-checks it
against the real dependency sources (vcpkg.json manifest + the ThirdParty/ tree),
and emits two deterministic artifacts at the repo root:

  * sbom.cyclonedx.json   - a CycloneDX 1.5 software bill of materials
  * THIRD_PARTY_NOTICES.md - a human-readable license inventory

Determinism: components are sorted and NO timestamp / random serial number is
emitted, so re-running produces byte-identical output. CI regenerates and
`git diff --exit-code`s these files to catch an inventory edited without a
regenerate, and this script exits non-zero on structural drift (a dependency
present in vcpkg.json / ThirdParty/ but absent from the inventory, or vice
versa). Stdlib only - no third-party Python packages required.
"""

import argparse
import json
import os
import sys


def repo_root_default():
    # tools/deps/generate_sbom.py -> repo root is two levels up.
    return os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))


def load_json(path):
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def license_node(dep):
    """CycloneDX license object for one dependency."""
    spdx = dep.get("spdx", "NOASSERTION")
    name = dep.get("license_name")
    if name or spdx.startswith("LicenseRef-"):
        return {"license": {"name": name or spdx}}
    if " OR " in spdx or " AND " in spdx:
        return {"expression": spdx}
    return {"license": {"id": spdx}}


def external_refs(dep):
    refs = []
    if dep.get("homepage"):
        refs.append({"type": "website", "url": dep["homepage"]})
    lf = dep.get("license_file")
    if lf:
        refs.append({"type": "license", "url": lf})
    return refs


def to_component(dep, source):
    comp = {
        "type": "application" if dep.get("name") == "vcpkg" else "library",
        "name": dep["name"],
        "version": dep.get("version", "unknown"),
        "licenses": [license_node(dep)],
    }
    if dep.get("purl"):
        comp["purl"] = dep["purl"]
    refs = external_refs(dep)
    if refs:
        comp["externalReferences"] = refs
    props = [{"name": "hexengine:source", "value": source}]
    if dep.get("notes"):
        props.append({"name": "hexengine:notes", "value": dep["notes"]})
    comp["properties"] = props
    return comp


def build_sbom(inv):
    proj = inv["project"]
    vcpkg_ver = "vcpkg-baseline:" + proj.get("vcpkg_baseline", "unknown")

    comps = []
    for dep in inv.get("vcpkg", []):
        d = dict(dep)
        d.setdefault("version", vcpkg_ver)
        comps.append(to_component(d, "vcpkg"))
    for dep in inv.get("thirdparty", []):
        comps.append(to_component(dep, "thirdparty"))

    comps.sort(key=lambda c: (c["properties"][0]["value"], c["name"].lower()))

    return {
        "bomFormat": "CycloneDX",
        "specVersion": "1.5",
        "version": 1,
        "metadata": {
            "component": {
                "type": "application",
                "name": proj["name"],
                "version": proj.get("version", "0.0.0"),
            }
        },
        "components": comps,
    }


def build_notices(inv):
    proj = inv["project"]
    lines = []
    lines.append("# Third-Party Notices")
    lines.append("")
    lines.append(
        "This file is GENERATED by `tools/deps/generate_sbom.py` from "
        "`tools/deps/dependencies.json`. Do not edit by hand - update the "
        "inventory and regenerate."
    )
    lines.append("")
    lines.append(
        "HexEngine bundles / links the third-party components listed below. "
        "Each is distributed under its own license; the full text lives in the "
        "referenced license file."
    )
    lines.append("")

    def table(title, deps, is_vcpkg):
        out = [f"## {title}", ""]
        out.append("| Component | Version | License (SPDX) | Homepage | License file |")
        out.append("| --- | --- | --- | --- | --- |")
        for dep in sorted(deps, key=lambda d: d["name"].lower()):
            ver = dep.get("version", "vcpkg-baseline:" + proj.get("vcpkg_baseline", "")) if is_vcpkg else dep.get("version", "vendored")
            home = dep.get("homepage", "")
            home_md = f"[link]({home})" if home else ""
            if dep.get("license_file"):
                lf_md = f"`{dep['license_file']}`"
            elif is_vcpkg:
                lf_md = "_(installed via vcpkg)_"
            elif dep.get("license_name"):
                lf_md = "_(vendor SDK EULA)_"
            else:
                lf_md = "—"
            out.append(f"| {dep['name']} | {ver} | {dep.get('spdx','NOASSERTION')} | {home_md} | {lf_md} |")
        out.append("")
        return out

    lines += table("vcpkg manifest dependencies", inv.get("vcpkg", []), True)
    lines += table("Vendored dependencies (ThirdParty/)", inv.get("thirdparty", []), False)
    return "\n".join(lines) + "\n"


def validate(inv, root):
    """Cross-check the inventory against reality. Returns (errors, warnings)."""
    errors, warnings = [], []

    # vcpkg.json <-> inventory.vcpkg
    vcpkg_json = load_json(os.path.join(root, "vcpkg.json"))
    manifest_deps = set()
    for d in vcpkg_json.get("dependencies", []):
        manifest_deps.add(d["name"] if isinstance(d, dict) else d)
    inv_vcpkg = {d["name"] for d in inv.get("vcpkg", [])}
    for name in sorted(manifest_deps - inv_vcpkg):
        errors.append(f"vcpkg dependency '{name}' is in vcpkg.json but MISSING from the inventory")
    for name in sorted(inv_vcpkg - manifest_deps):
        warnings.append(f"inventory lists vcpkg dependency '{name}' that is not in vcpkg.json")

    # ThirdParty/ dirs <-> inventory.thirdparty
    tp_root = os.path.join(root, "ThirdParty")
    on_disk = set()
    if os.path.isdir(tp_root):
        on_disk = {d for d in os.listdir(tp_root) if os.path.isdir(os.path.join(tp_root, d))}
    inv_dirs = {d["dir"] for d in inv.get("thirdparty", [])}
    ignored = set(inv.get("ignore_thirdparty_dirs", {}).keys())
    for d in sorted(on_disk - inv_dirs - ignored):
        errors.append(f"ThirdParty/{d} exists on disk but is MISSING from the inventory (add it or list it in ignore_thirdparty_dirs)")
    for d in sorted(inv_dirs - on_disk):
        warnings.append(f"inventory lists ThirdParty/{d} that does not exist on disk")

    # License files + SPDX assertions
    for dep in inv.get("thirdparty", []):
        lf = dep.get("license_file")
        if lf:
            if not os.path.isfile(os.path.join(root, lf)):
                warnings.append(f"{dep['name']}: recorded license_file '{lf}' does not exist")
        elif not dep.get("license_name"):
            warnings.append(f"{dep['name']}: no license_file and no license_name recorded")
        if dep.get("spdx", "NOASSERTION") == "NOASSERTION":
            warnings.append(f"{dep['name']}: license is NOASSERTION (please classify)")

    return errors, warnings


def main():
    ap = argparse.ArgumentParser(description="Generate HexEngine dependency SBOM + notices.")
    ap.add_argument("--root", default=repo_root_default(), help="Repository root.")
    ap.add_argument("--inventory", default=None, help="Path to dependencies.json.")
    ap.add_argument("--check", action="store_true",
                    help="Validate only; do not write output files. Exits non-zero on drift.")
    args = ap.parse_args()

    root = os.path.abspath(args.root)
    inventory_path = args.inventory or os.path.join(root, "tools", "deps", "dependencies.json")
    inv = load_json(inventory_path)

    errors, warnings = validate(inv, root)

    for w in warnings:
        print(f"WARNING: {w}")
    for e in errors:
        print(f"ERROR: {e}")

    vcpkg_n = len(inv.get("vcpkg", []))
    tp_n = len(inv.get("thirdparty", []))
    print(f"Inventory: {vcpkg_n} vcpkg + {tp_n} vendored = {vcpkg_n + tp_n} components "
          f"({len(warnings)} warning(s), {len(errors)} error(s)).")

    if not args.check:
        sbom = build_sbom(inv)
        with open(os.path.join(root, "sbom.cyclonedx.json"), "w", encoding="utf-8", newline="\n") as f:
            json.dump(sbom, f, indent=2, ensure_ascii=False)
            f.write("\n")
        with open(os.path.join(root, "THIRD_PARTY_NOTICES.md"), "w", encoding="utf-8", newline="\n") as f:
            f.write(build_notices(inv))
        print("Wrote sbom.cyclonedx.json and THIRD_PARTY_NOTICES.md")

    return 1 if errors else 0


if __name__ == "__main__":
    sys.exit(main())
